# Operadores Aritmeticos
# + - * / % ** //

num1 = 4
num2 = 5

suma = num1 + num2
print(suma) # 9

resta = num1 - num2
print(resta) # -1

mult = num1 * num2
print(mult) # 20

div = num1 / num2
print(div) # 0.8

mod = num1 % num2
print(mod) # 4

exp = num1 ** num2
print(exp) # 1024

